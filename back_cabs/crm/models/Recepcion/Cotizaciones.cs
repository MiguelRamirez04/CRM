using System.ComponentModel.DataAnnotations;
using System.ComponentModel.DataAnnotations.Schema;

namespace back_cabs.CRM.models.Sales
{
    [Table("sales_cotizaciones")]  
    public class Cotizacion
    {
        [Key]
        public int Id { get; set; }

        [Column("orden_id")]
        public int? OrdenId { get; set; }

        [Column("intake_legacy_id")]
        public int? IntakeLegacyId { get; set; }

        [Column(TypeName = "decimal(12,2)")]
        public decimal Subtotal { get; set; } = 0;

        [Column("impuestos_total", TypeName = "decimal(12,2)")]
        public decimal ImpuestosTotal { get; set; } = 0;

        // ✅ NUEVO: Campo descuento
        [Column(TypeName = "decimal(12,2)")]
        public decimal? Descuento { get; set; }

        // ✅ ACTUALIZADO: Total ahora incluye descuento
        [NotMapped]
        public decimal Total => Subtotal + ImpuestosTotal - (Descuento ?? 0);

        [StringLength(20)]
        public string Estado { get; set; } = "NUEVA";

        [StringLength(500)]
        public string? Observaciones { get; set; }

        // ✅ NUEVO: Campo cliente
        [StringLength(255)]
        public string? Cliente { get; set; }

        // ✅ NUEVO: Campo RFC
        [StringLength(13)]
        public string? Rfc { get; set; }

        // ✅ NUEVO: Campo folio
        [StringLength(50)]
        public string? Folio { get; set; }

        // ✅ NUEVO: Campo descripción servicio (corregido de "descrpcion_servicio")
        [Column("descrpcion_servicio")]
        [StringLength(1000)]
        public string? DescripcionServicio { get; set; }

        [Column("actualizado_en")]
        public DateTime? ActualizadoEn { get; set; }

        [Column("creado_en")]
        public DateTime CreadoEn { get; set; } = DateTime.UtcNow;

        [Column("validez_dias")]
        public int? ValidezDias { get; set; }
    }
}



















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































